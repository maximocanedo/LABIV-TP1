package ejercicio1;

public class Empleado {
	/* Properties */
	private int id;
	private String nombre;
	private int edad;
	
	
	
	/* Getters */
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	/* Setters */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getEdad() {
		return edad;
	}
	public void setEdad(int edad) {
		this.edad = edad;
	}
	
}
